package entities;

public class BusinessAccount_2 extends Account_2 {
	
	private Double loanLimit;
	
	public BusinessAccount_2(){
		super();
	}

	public BusinessAccount_2(Integer number, String holder, Double balance, Double loanLimit) {
		super(number, holder, balance);
		this.loanLimit = loanLimit;
	}

	public Double getLoanLimit() {
		return loanLimit;
	}

	public void setLoanLimit(Double loanLimit) {
		this.loanLimit = loanLimit;
	}
	
	public void loan(double amount) {
		if (amount <= loanLimit) {
			balance += amount - 10.0;
		}
	}

}
